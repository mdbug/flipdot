def write(frame: np.array, text: str, x=0, y=0, size=6, spacing=1, color=1):
    font = FONTS[size]
    for char in text:
        c = font[char]
        y1 = min(y+c.shape[0], frame.shape[0])
        x1 = min(x+c.shape[1], frame.shape[1])
        if color == 1:
            frame[y:y1,x:x1] = c[0:y1-y,0:x1-x]
        else:
            frame[y:y1,x:x1] = np.logical_not(c[0:y1-y,0:x1-x])
        x = x1+spacing
        if x >= frame.shape[1]:
            break